        if cp >= 0x014A && cp <= 0x0177 && cp & 1 == 1 { return cp - 1 }
        if cp >= 0x0179 && cp <= 0x017E && cp & 1 == 0 { return cp - 1 }
        // Greek
        if cp >= 0x03B1 && cp <= 0x03C1 { return cp - 32 }
        if cp >= 0x03C3 && cp <= 0x03C9 { return cp - 32 }
        if cp == 0x03AC { return 0x0386 }
